#!/usr/bin/env node
/**
 * Pulls the Sakay catalog out of Airtable and writes typed TS files
 * that lib/mock.ts re-exports. Run locally with `npm run sync`, or wire
 * it into a build step on Vercel (it runs in `prebuild`).
 *
 * Required env:
 *   AIRTABLE_API_KEY   personal access token with data.records:read on the base
 *   AIRTABLE_BASE_ID   e.g. appxxnxrfm14M9ZmV
 *
 * Without those env vars, the script logs and exits 0 so the build still
 * succeeds against the bundled fallback data already in lib/data/*.generated.ts.
 */
import { writeFile, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const OUT_DIR = resolve(ROOT, "lib/data");

const API_KEY = process.env.AIRTABLE_API_KEY;
const BASE_ID = process.env.AIRTABLE_BASE_ID;
const CARS_TABLE = process.env.AIRTABLE_CARS_TABLE || "Cars";
const HOSTS_TABLE = process.env.AIRTABLE_HOSTS_TABLE || "Hosts";
const REVIEWS_TABLE = process.env.AIRTABLE_REVIEWS_TABLE || "Reviews";
const BOOKINGS_TABLE = process.env.AIRTABLE_BOOKINGS_TABLE || "Bookings";

if (!API_KEY || !BASE_ID) {
  console.warn("[sync-airtable] AIRTABLE_API_KEY / AIRTABLE_BASE_ID not set — skipping. Using fallback data.");
  process.exit(0);
}

async function fetchAll(table) {
  const records = [];
  let offset;
  do {
    const url = new URL(`https://api.airtable.com/v0/${BASE_ID}/${encodeURIComponent(table)}`);
    if (offset) url.searchParams.set("offset", offset);
    const res = await fetch(url, {
      headers: { Authorization: `Bearer ${API_KEY}` },
    });
    if (!res.ok) {
      const body = await res.text();
      throw new Error(`Airtable ${table} fetch failed (${res.status}): ${body}`);
    }
    const json = await res.json();
    records.push(...json.records);
    offset = json.offset;
  } while (offset);
  return records;
}

// Photos seeded in Airtable use a canonical absolute URL; map back to the
// /public path so <Image> serves them locally without remote allowlisting.
function normalizePhoto(url) {
  if (!url) return "";
  const m = url.match(/^https?:\/\/sakay\.app(\/cars\/.+)$/);
  return m ? m[1] : url;
}

function splitFeatures(text) {
  return (text || "").split(/\r?\n/).map((s) => s.trim()).filter(Boolean);
}

function toCar(rec, hostsByName) {
  const f = rec.fields;
  const host = hostsByName.get(f.Host) ?? {};
  return {
    id: f.ID,
    make: f.Make,
    model: f.Model,
    year: f.Year,
    category: f.Category,
    pricePerDay: f["Price per day"],
    rating: f.Rating,
    trips: f.Trips,
    location: f.Location,
    hostName: f.Host,
    hostPhoto: host.photo || "https://i.pravatar.cc/150?img=33",
    hostSuperhost: !!host.superhost,
    photo: normalizePhoto(f.Photo),
    features: splitFeatures(f.Features),
    instantBook: !!f["Instant book"],
    driverOption: f["Driver option"],
    ...(f["Black only"] ? { blackOnly: true } : {}),
    ...(f["Region tag"] ? { regionTag: f["Region tag"] } : {}),
  };
}

function toHost(rec) {
  const f = rec.fields;
  return {
    name: f.Name,
    photo: f.Photo,
    superhost: !!f.Superhost,
    verified: !!f.Verified,
    bio: f.Bio,
    basedIn: f["Based in"],
  };
}

function toReview(rec) {
  const f = rec.fields;
  return {
    id: f.ID,
    carId: f["Car ID"],
    reviewerName: f.Reviewer,
    reviewerPhoto: f["Reviewer photo"],
    rating: f.Rating,
    date: f.Date,
    text: f.Text,
  };
}

function toActivity(rec) {
  const f = rec.fields;
  return {
    id: f.ID,
    type: f.Type,
    title: f.Title,
    subtitle: f.Subtitle,
    date: f.Date,
    status: f.Status,
    amount: f.Amount ?? 0,
    photo: normalizePhoto(f.Photo),
  };
}

const idSort = (a, b) => a.id.localeCompare(b.id, "en", { numeric: true });

async function writeGenerated(name, header, body) {
  const path = resolve(OUT_DIR, `${name}.generated.ts`);
  const banner = `// AUTO-GENERATED by scripts/sync-airtable.mjs — do not edit by hand.
// Re-run with: npm run sync
// Source: Airtable base ${BASE_ID}
`;
  await mkdir(OUT_DIR, { recursive: true });
  await writeFile(path, `${banner}\n${header}\n\n${body}\n`);
  return path;
}

const [carRecs, hostRecs, reviewRecs, bookingRecs] = await Promise.all([
  fetchAll(CARS_TABLE),
  fetchAll(HOSTS_TABLE),
  fetchAll(REVIEWS_TABLE),
  fetchAll(BOOKINGS_TABLE),
]);

const hosts = hostRecs.map(toHost);
const hostsByName = new Map(hosts.map((h) => [h.name, h]));

const cars = carRecs.map((r) => toCar(r, hostsByName)).filter((c) => c.id && c.make && c.model).sort(idSort);
const reviews = reviewRecs.map(toReview).filter((r) => r.id && r.carId).sort(idSort);
const activity = bookingRecs.map(toActivity).filter((a) => a.id && a.title).sort(idSort);

await writeGenerated(
  "cars",
  `import type { Car } from "../mock";`,
  `export const cars: Car[] = ${JSON.stringify(cars, null, 2)};`,
);
await writeGenerated(
  "reviews",
  `import type { Review } from "../mock";`,
  `export const reviews: Review[] = ${JSON.stringify(reviews, null, 2)};`,
);
await writeGenerated(
  "activity",
  `import type { Activity } from "../mock";`,
  `export const activity: Activity[] = ${JSON.stringify(activity, null, 2)};`,
);

console.log(`[sync-airtable] Wrote ${cars.length} cars, ${reviews.length} reviews, ${activity.length} bookings.`);

#!/usr/bin/env node
/**
 * Pulls the Sakay car catalog out of Airtable and writes a typed TS file
 * that `lib/mock.ts` re-exports. Run locally with `npm run sync`, or wire
 * it into a build step on Vercel (it runs in `prebuild`).
 *
 * Required env:
 *   AIRTABLE_API_KEY   personal access token with data.records:read on the base
 *   AIRTABLE_BASE_ID   e.g. appxxnxrfm14M9ZmV
 *
 * Without those env vars, the script logs and exits 0 so the build still
 * succeeds against the bundled fallback data in lib/mock.ts.
 */
import { writeFile, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const OUT_PATH = resolve(ROOT, "lib/data/cars.generated.ts");

const API_KEY = process.env.AIRTABLE_API_KEY;
const BASE_ID = process.env.AIRTABLE_BASE_ID;
const CARS_TABLE = process.env.AIRTABLE_CARS_TABLE || "Cars";
const HOSTS_TABLE = process.env.AIRTABLE_HOSTS_TABLE || "Hosts";

if (!API_KEY || !BASE_ID) {
  console.warn("[sync-airtable] AIRTABLE_API_KEY / AIRTABLE_BASE_ID not set — skipping. Using fallback data.");
  process.exit(0);
}

async function fetchAll(table) {
  const records = [];
  let offset;
  do {
    const url = new URL(`https://api.airtable.com/v0/${BASE_ID}/${encodeURIComponent(table)}`);
    if (offset) url.searchParams.set("offset", offset);
    const res = await fetch(url, {
      headers: { Authorization: `Bearer ${API_KEY}` },
    });
    if (!res.ok) {
      const body = await res.text();
      throw new Error(`Airtable ${table} fetch failed (${res.status}): ${body}`);
    }
    const json = await res.json();
    records.push(...json.records);
    offset = json.offset;
  } while (offset);
  return records;
}

// Photos seeded in Airtable use a canonical absolute URL; map back to the
// /public path so <Image> serves them locally without remote allowlisting.
function normalizePhoto(url) {
  if (!url) return "";
  const m = url.match(/^https?:\/\/sakay\.app(\/cars\/.+)$/);
  return m ? m[1] : url;
}

function splitFeatures(text) {
  return (text || "")
    .split(/\r?\n/)
    .map((s) => s.trim())
    .filter(Boolean);
}

function toCar(rec, hostsByName) {
  const f = rec.fields;
  const host = hostsByName.get(f.Host) ?? {};
  return {
    id: f.ID,
    make: f.Make,
    model: f.Model,
    year: f.Year,
    category: f.Category,
    pricePerDay: f["Price per day"],
    rating: f.Rating,
    trips: f.Trips,
    location: f.Location,
    hostName: f.Host,
    hostPhoto: host.photo || "https://i.pravatar.cc/150?img=33",
    hostSuperhost: !!host.superhost,
    photo: normalizePhoto(f.Photo),
    features: splitFeatures(f.Features),
    instantBook: !!f["Instant book"],
    driverOption: f["Driver option"],
    ...(f["Black only"] ? { blackOnly: true } : {}),
    ...(f["Region tag"] ? { regionTag: f["Region tag"] } : {}),
  };
}

function toHost(rec) {
  const f = rec.fields;
  return {
    name: f.Name,
    photo: f.Photo,
    superhost: !!f.Superhost,
    verified: !!f.Verified,
    bio: f.Bio,
    basedIn: f["Based in"],
  };
}

const [carRecs, hostRecs] = await Promise.all([fetchAll(CARS_TABLE), fetchAll(HOSTS_TABLE)]);
const hosts = hostRecs.map(toHost);
const hostsByName = new Map(hosts.map((h) => [h.name, h]));
const cars = carRecs
  .map((r) => toCar(r, hostsByName))
  .filter((c) => c.id && c.make && c.model)
  .sort((a, b) => a.id.localeCompare(b.id, "en", { numeric: true }));

const header = `// AUTO-GENERATED by scripts/sync-airtable.mjs — do not edit by hand.
// Re-run with: npm run sync
// Source: Airtable base ${BASE_ID}
`;

const body = `import type { Car } from "../mock";

export const cars: Car[] = ${JSON.stringify(cars, null, 2)};
`;

await mkdir(dirname(OUT_PATH), { recursive: true });
await writeFile(OUT_PATH, header + "\n" + body);

console.log(`[sync-airtable] Wrote ${cars.length} cars (${hosts.length} hosts) to ${OUT_PATH}`);
